# 반복분 사용 하기

for i in range(10):
    print('hello world!')  # hello world 10번 출력


for i in range(10):
    print('hello world!', i)  # 10번 출력 및 0-9


for i in range(5, 12):    # 5부터 11까지 반복
    print('Hello, world!', i)


for i in range(0, 10, 2):    # 0부터 8까지 2씩 증가
    print('Hello, world!', i)


for i in range(10, 0, -1):    # 10에서 1까지 1씩 감소
     print('Hello, world!', i)

